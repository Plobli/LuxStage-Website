function detectLanguage() {
  const saved = localStorage.getItem('luxstage-lang');
  if (saved && translations[saved]) return saved;
  const browser = (navigator.language || navigator.userLanguage || 'de').split('-')[0].toLowerCase();
  return translations[browser] ? browser : 'de';
}

function setLanguage(lang) {
  if (!translations[lang]) return;
  localStorage.setItem('luxstage-lang', lang);
  document.documentElement.lang = lang;
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (translations[lang][key] !== undefined) {
      if (el.hasAttribute('data-i18n-html')) {
        el.innerHTML = translations[lang][key];
      } else {
        el.textContent = translations[lang][key];
      }
    }
  });
  document.querySelectorAll('[data-i18n-title]').forEach(el => {
    const key = el.getAttribute('data-i18n-title');
    if (translations[lang][key] !== undefined) el.title = translations[lang][key];
  });
  document.querySelectorAll('[data-i18n-aria]').forEach(el => {
    const key = el.getAttribute('data-i18n-aria');
    if (translations[lang][key] !== undefined) el.setAttribute('aria-label', translations[lang][key]);
  });
  // Update page title
  const titleKey = document.documentElement.getAttribute('data-i18n-page-title');
  if (titleKey && translations[lang][titleKey]) {
    document.title = translations[lang][titleKey];
  }
  // Update lang switcher button states
  document.querySelectorAll('[data-lang-btn]').forEach(btn => {
    const isActive = btn.getAttribute('data-lang-btn') === lang;
    btn.classList.toggle('text-white', isActive);
    btn.classList.toggle('bg-white/10', isActive);
    btn.classList.toggle('text-gray-500', !isActive);
  });
}

function initI18n() {
  const lang = detectLanguage();
  setLanguage(lang);
}

window.setLanguage = setLanguage;
